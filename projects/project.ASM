JMP START		
DB 4			
DB 10
IRET

START:
STI
OUT 07
JMP START

ORG 10
CLI
CALL 90
IN 07
cmp al, 0d

jz koniec
jmp wypisz

WYPISZ:
MOV BL,26
MOV [C0],BL
SUB BL,3
MOV [C1],BL
PUSH AL ; 3a
DIV AL,A
ADD AL,30
MOV [C2],AL
POP AL ; 3a
MOD AL,A
ADD AL,30
MOV [C3],AL
IRET

KONIEC:
MOV [C0],AL
IRET

ORG 90
MOV AL,20
MOV CL,C3
PETLA:
CMP CL,BF
JZ POWROT
MOV [CL],AL
DEC CL
JMP PETLA
POWROT:
RET

END 