mov dl, a0
mov cl,c0 ; присваем дл и сл адресы в оперативной памяти
start:
in 00 ; вчитываем знак с клавиатуры в реестр аль
call 14 
call 30
call 50 ; вызываем процедуры для выполнения следующих действий
cmp al,0d ; сравниваем аль = enter, чтобы остановить программу 
jnz start ; если не нажали enter, то продолжает программу

org 14 ; первая процедура по сохранению в оперативную память заглавного знака.
mov [dl],al ; а0 принимает значение реестра аль ( он же знак из таблицы аски ).H=48, h=68
ret ; переход к call 30

org 30
mov bl, [dl] ; считываем сохраненный заглавный знак из оперативной памяти в реест бл
add bl, 20 ; добавляем заглавному знаку 20, чтобы он стал строчным знаком.
mov [dl], bl ; сохраняем строчный знак
inc dl ; переходим к следующей ячейке в памяти
ret ; переход к call 50

org 50
mov [cl],bl ; выводим строчный знак на экран в адресе цл
inc cl ; переходим в следующую ячейку цл
ret ; переход к cmp al,0d

end ; конец программы
